class Persona(object):
   


    def __init__(self, dni, nombre, apellidos, direccion, telefono, email):
        self.dni = dni
        self.nombre = nombre
        self.apellidos = apellidos
        self.direccion = direccion
        self.telefono = telefono
        self.email = email
